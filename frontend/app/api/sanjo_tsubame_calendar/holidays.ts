// 特定の日付が休日かどうかを判定する関数
export function isHoliday(year: string | number, month: string | number, date: string | number): boolean {
    const yearStr = year.toString();
    const monthStr = month.toString();

    if (
        HOLIDAYS[yearStr] &&
        HOLIDAYS[yearStr][monthStr]
    ) {
        const dateNum = parseInt(date.toString());
        return HOLIDAYS[yearStr][monthStr].includes(dateNum);
    }

    return false;
}

// 営業状態を取得する関数
export function getBusinessStatus(year: string | number, month: string | number, date: string | number): string {
    const yearStr = year.toString();
    const monthStr = month.toString();

    if (
        HOLIDAYS[yearStr] &&
        HOLIDAYS[yearStr][monthStr]
    ) {
        return isHoliday(yearStr, monthStr, date) ? "off" : "on";
    }

    return "undefined";
} 